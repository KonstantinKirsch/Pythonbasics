summe = 10 + 5         # Wenn man in einer Print Funktion ein String und ein Integer 
print("Summe:", summe) # Verbinden möchte braucht man ein komma (,) // Addition

differenz = 10 - 5
print("Differenz:", differenz) # // Subtraktion

produkt = 10 * 5 # Ateris = * // Multiplikation
print("Produkt:", produkt)

quotient = 10 / 5 # Ausgabe in Float // Division
print("Quotient:", quotient)

rest = 10 % 3 # Modulus Operator // Modulus = rest den wir zurück bekommen
print("Rest:", rest) # = 1 Restwert

kombination = (10 + 5) * (4 - 2) # Kombination von Operatoren
print("Ergebnis", kombination)




